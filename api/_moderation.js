// Базовый фильтр от мата и спама для комментариев и имён.
// Это НЕ идеальное решение — ловит по корням слов (без учёта всех форм и обходов
// вроде "пр0клятие" или "х.й"), поэтому список стоит время от времени пополнять
// по факту того, что реально проходит. Для более серьёзной защиты в будущем
// имеет смысл подключить специализированный npm-пакет фильтрации мата/спама.
//
// Файл начинается с "_", поэтому Vercel не создаёт из него отдельный HTTP-роут.

const BANNED_ROOTS = [
  // Русский мат — основные корни (без учёта всех словоформ намеренно, чтобы не плодить ложные срабатывания)
  'хуй', 'хуе', 'хуё', 'пизд', 'ебат', 'ёбан', 'еблан', 'бляд', 'блять',
  'сука', 'мудак', 'мудил', 'гандон', 'долбоеб', 'долбоёб', 'пидор', 'пидар',
  'залуп', 'манда', 'ссан', 'гнида', 'ублюдок', 'шлюх', 'чмо',
  // Английские аналоги
  'fuck', 'shit', 'bitch', 'asshole', 'cunt', 'nigger', 'faggot',
  // Типичные спам-паттерны
  'казино', 'casino', 'viagra', 'виагра', 'заработок на дому', 'быстрый заработок',
  'crypto airdrop', 'бесплатные подписчики',
];

// Частые замены символов, которыми обходят простые фильтры (leetspeak / визуально похожие символы)
const SUBSTITUTIONS = {
  '0': 'о', '1': 'i', '3': 'е', '4': 'a', '5': 's', '7': 't', '@': 'a',
  '$': 's', '!': 'i',
};

function normalize(text) {
  let result = text.toLowerCase();
  for (const [from, to] of Object.entries(SUBSTITUTIONS)) {
    result = result.split(from).join(to);
  }
  // Убираем не-буквенные разделители, которыми часто "разбивают" запрещённые слова (х*й, х.у.й)
  result = result.replace(/[^a-zа-яё0-9]/g, '');
  return result;
}

/**
 * Возвращает true, если текст содержит запрещённые слова/спам-паттерны.
 * Не сообщает, какое именно слово сработало — чтобы не подсказывать способ обхода.
 */
export function containsBannedContent(text) {
  if (!text) return false;
  const normalized = normalize(text);
  return BANNED_ROOTS.some(root => normalized.includes(normalize(root)));
}
